package com.softcell.routing.workflow.dataObject;

public class ActionData {

	public int ID;
	
	public String type;
	
	public int Value ;

	public int getID() {
		return ID;
	}

	public void setID(int iD) {
		ID = iD;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public int getValue() {
		return Value;
	}

	public void setValue(int value) {
		Value = value;
	}
	
	
}
